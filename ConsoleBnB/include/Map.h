#pragma once

#include "Types.h"
#include <array>
#include <random>

// =================================================================
// 게임의 무대와 보물 지도를 총괄하는 전장 관리자 (클래스)
// =================================================================
// 맵에 벽이 어디 있고, 상자가 어디 있는지, 상자가 부서지면 
// 그 자리에 무슨 아이템이 숨겨져 있는지 2차원 배열로 완벽히 기억하는 총책임자야.
class GameMap
{
public:
    // [생성자] 게임 지도를 처음 만들 때 호출되며, 내부 맵 데이터를 초기화해.
    GameMap();

    // 맵의 모든 벽과 상자 배치, 아이템 보물 지도를 처음 상태로 깨끗이 리셋하는 함수야.
    void reset();

    // 지정한 바둑판 주소(p)에 어떤 타일(빈길, 벽, 상자)이 놓여있는지 알려주는 스캐너 함수야.
    Tile tileAt(Vec2 p) const;

    // 지정한 바둑판 주소(p)의 바닥에 어떤 아이템이 떨어져 있는지 알려주는 스캐너 함수야.
    ItemType itemAt(Vec2 p) const;

    // 특정 바둑판 주소(p)의 타일 속성을 강제로 변경하는 함수야. (예: 상자가 부서져서 빈길로 바뀔 때 사용)
    void setTile(Vec2 p, Tile tile);

    // 특정 바둑판 주소(p)의 바닥에 아이템을 배치하거나 치워버리는 함수야.
    void setItem(Vec2 p, ItemType item);

    // 입력받은 주소(p)가 17x13 모눈종이 안쪽이 맞는지, 맵 바깥(화면 탈출)인지 판정해 주는 안전장치 함수야.
    bool inBounds(Vec2 p) const;

    // 플레이어가 이 칸(p)으로 걸어갈 수 있는 빈길인지 확인해 주는 길잡이 함수야.
    // (벽이나 상자가 있으면 false를 반환해서 몸으로 막아버려.)
    bool isWalkable(Vec2 p) const;

    // [핵심 액션 함수] 물줄기가 상자를 때렸을 때 호출되는 함수야.
    // 상자를 부숴서 빈길(Empty)로 만들고, 난수 생성기(rng)를 돌려 확률적으로 아이템을 바닥에 떨어트려 줘.
    bool breakBlock(Vec2 p, std::mt19937& rng);

private:
    // =================================================================
    // 맵의 비밀 데이터 장부 (2차원 고정 배열)
    // =================================================================

    // 가로 17칸, 세로 13칸짜리 바둑판 바닥 격자 장부야. (벽, 상자 위치 기록)
    std::array<std::array<Tile, MAP_W>, MAP_H> tiles_{};

    // 바닥 장부랑 똑같은 크기의 숨겨진 보물 지도 장부야. (어느 상자 밑에 무슨 아이템이 숨어있는지 기록)
    std::array<std::array<ItemType, MAP_W>, MAP_H> items_{};

    // [예외 처리 함수] 플레이어의 리스폰 시작점(start) 주변 십자 지역을 강제로 깨끗하게 청소해 주는 함수야.
    // 게임을 시작하자마자 사방이 상자로 꽉 막혀서 아예 한 발짝도 움직이지 못하고 갇히는 억까 버그를 원천 차단해 줘.
    void clearStartArea(Vec2 start);
};